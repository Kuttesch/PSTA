package th.rosenheim.oop;
/**
 * The Wetter enum represents the different weather conditions used in the WebsiteGenerator.
 * It has three values: SONNIG, WOLKIG and REGNERISCH.
 * It is used throughout the project to represent the weather in a city.
 * **/

public enum Wetter {
    /**
     * The SONNIG value represents a sunny weather condition.
     * **/
    SONNIG,
    /**
     * The WOLKIG value represents a cloudy weather condition.
     * **/
    WOLKIG,
    /**
     * The REGNERISCH value represents a rainy weather condition.
     * **/
    REGNERISCH;
}
